def display_rainbow(colors)
  rainbow = "R: #{colors[0]}, "
  rainbow << "O: #{colors[1]}, "
  rainbow << "Y: #{colors[2]}, "
  rainbow << "G: #{colors[3]}, "
  rainbow << "B: #{colors[4]}, "
  rainbow << "I: #{colors[5]}, "
  rainbow << "V: #{colors[6]}"
  puts rainbow
end