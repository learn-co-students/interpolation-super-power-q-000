def display_rainbow(peace)
  print "R: #{peace[0]}, "
  print "O: #{peace[1]}, "
  print "Y: #{peace[2]}, "
  print "G: #{peace[3]}, "
  print "B: #{peace[4]}, "
  print "I: #{peace[5]}, "
  print "V: #{peace[6]}\n"
end
color = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']
display_rainbow(color)